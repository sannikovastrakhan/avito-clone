# Моя первая программа для хранения объявлений

# Создаем список объявлений (пока просто словари)
ads = [
    {"title": "Продам MacBook Air M1", "price": 80000, "category": "Электроника"},
    {"title": "Куплю велосипед", "price": 15000, "category": "Спорт"},
    {"title": "Сдам квартиру", "price": 40000, "category": "Недвижимость"}
]

# Выводим все объявления
print("=== Мои объявления ===\n")

for ad in ads:
    print(f"Название: {ad['title']}")
    print(f"Цена: {ad['price']} ₽")
    print(f"Категория: {ad['category']}")
    print("-" * 30)

print(f"Всего объявлений: {len(ads)}")


